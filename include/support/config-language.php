    // dil desteğine göre metin çıktısı veren fonksiyon
    public static function getlang(int $keynum, $langnum = 0) {
        // anahtar oluşturulmamışsa veya girilen anahat oluşturulmamışsa veya
        // girilen numarada bir değer oluşturulmamışsa
        // veyaaa girilen anahtardaki değer boş ise "n/a" dönsün değilse değer dönsün

        // fonksiyon için ek parametre kontrolleri
        if(isset($keynum) != true || isset($langnum) != true || $keynum < 0 || $langnum < 0) {
            return false;
        }

        // oturuma ait dil geçersizse boş dönsün
        if((SessionManager::get(SessionManager::$session_var_language)) < 1) {
            return false;
        }

        // herhangi bir dil girmemişse otomatik oturum diline ayarla
        if($langnum < 1) {
            $langnum = SessionManager::get(SessionManager::$session_var_language);
        }

        // istenilen dil için çeviri yoksa n/a çıktısı versin
        switch(empty(trim(LanguageSupport::$LANGUAGE_SUPP_LIST[$keynum][$langnum])) == false) {
            // dil desteği var
            case 1:
                return LanguageSupport::$LANGUAGE_SUPP_LIST[$keynum][$langnum];
            // dil desteği yok
            default:
                return "n/a";
        }

        // herhangi bir şey yoksa boş dönsün
        return "";
    }
